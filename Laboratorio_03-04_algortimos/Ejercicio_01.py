texto = "python"

 # Encontramos la longitud del texto
longitud = len(texto)  
# Convertimos a la longitud a float
longitud_float = float(longitud) 
 # Convertimos la longitud float a string
longitud_string = str(longitud_float) 

print("Longitud del texto:", longitud)
print("Longitud como float:", longitud_float)
print("Longitud como string:", longitud_string)